import { fetchPageData } from "@/lib/page";
import CaseStudySectionClient from "../CaseStudySection";

export default async function CaseStudySectionServer() {
  const uid = "194f52e6-ffd8-4371-bea5-3c6fd72dbad5";
  let pageData = null;

  try {
    const res = await fetchPageData({ uid });

    if (
      Array.isArray(res?.pageItemdataWithSubsection) &&
      res.pageItemdataWithSubsection.length > 0
    ) {
      pageData = res.pageItemdataWithSubsection[0];
    }
  } catch (error) {
    console.error("Error in CaseStudySectionServer:", error);
  }

  return <CaseStudySectionClient data={pageData} />;
}
